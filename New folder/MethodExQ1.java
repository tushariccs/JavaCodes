public class MethodExQ1 {
    public static int avgOfThreeNumbers(int a, int b, int c) {
        return (a + b + c) / 3;
    }

    public static void main(String[] args) {
        int ans = avgOfThreeNumbers(5, 6, 50);
        System.out.println("The average of three numbers is = " + ans);

    }
}