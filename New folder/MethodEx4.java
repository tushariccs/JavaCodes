
public class MethodEx4 {
    public static void main(String[] args) {
        int a = Math.min(5, 6);
        System.out.println("Minimum number is:" + a);
        int b = Math.max(5, 6);
        System.out.println("Maximum number is:" + b);
        double c = Math.sqrt(25);
        System.out.println("Sqrt of 25 is:" + c);
        double v = Math.pow(2, 5);
        System.out.println("Pow of 2 is:" + v);
        double e = Math.abs(656);
        System.out.println("Abs of 656.154 is:" + e);
    }

}
